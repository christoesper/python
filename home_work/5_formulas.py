# area of a circle
radius = 20
pi = 3.14
squared = 2
# area = pi x (r**2)
area = (radius**squared) *pi
print("(",radius, "**", squared,")", "*", pi, "=", area)
# OR
print(f"({radius} ** {squared} * {pi} = {area})")


