# examples of arithmetic operators
# Q1: question = 2 + 5 // 5 * 3 = 5

question = 2 + 5 // 5 * 3
print(question)

#Q2: question2 = 2 + 5 // 5 * 3 - 1 = 4

question2 = 2 + 5 // 5 * 3 - 1
print(question2)

# Q3: question3 = 2 + 5 // 5 ** 3 - 4 = -2.04
# 5**3 = 125
# 5 // 125 = 0
# 2 + 0 = 2
# 2-4 = -2
question3 = 2 + 5 // 5 ** 3 - 4
print(question3)

# Q4: question4 = 10 - 8 % 2 ** 4 + 3 = 5

question4 = 10 - 8 % 2 ** 4 + 3
print(question4)